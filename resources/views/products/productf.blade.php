                    <h2>Standard Features</h2>
                    <ul id="advantages">
                        <li>1200&deg;C or 1300&deg;C maximum operating temperature</li>
                        <li>36, 65, 131 or 200 litre chamber volumes</li>
                        <li>Free radiating coiled wire elements</li>
                        <li>low thermal mass insulation for high energy efficiency</li>
                        <li>Up & away door keeps heated surface away from the user</li>
                        <li>Carbolite 301 controller, with single ramp to set-point & process timer</li>
                        <li>Hard wearing refractory hearth plate resists damage & supports heavier loads</li>
                        <li>Heating elements are easily serviced</li>
                    </ul>

